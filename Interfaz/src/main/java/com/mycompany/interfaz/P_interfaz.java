    private void factoInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_factoInActionPerformed
        // TODO add your handling code here:
        int i, factorial = 1;
        double in = 1;
        
        try{
        in = Double.parseDouble(factoIn.getText());
            if(in>0){
                for(i=1;i<=in;i++){
                factorial = factorial * i;
                factoOut.setText("Factorial de "+in+" = "+factorial);
            }
            }
            else{
            factoOut.setText("Error, entre solo numeros > 0");
            }
        }
        catch (NumberFormatException ex){
            in = 1;
            factoOut.setText("Error, entre solo numeros");
        }
        
    }//GEN-LAST:event_factoInActionPerformed

    private void eqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eqActionPerformed
        // TODO add your handling code here:
        
                if(preval==0){
                    result = Double.parseDouble(print.getText());
                }
                switch(calc){
                    case 1:
                        try{
                        result = (preval+(Double.parseDouble(print.getText())));
                        }
                        catch (NumberFormatException ex){
                        result = 0;
                        } break;
                    case 2:
                        try{
                        result = (preval-(Double.parseDouble(print.getText())));
                        }
                        catch (NumberFormatException ex){
                        result = 0;
                        } break;
                    case 3:
                        try{
                        result = (preval*(Double.parseDouble(print.getText())));
                        }
                        catch (NumberFormatException ex){
                        result = 0;
                        } break;
                    case 4:
                        try{
                        result = (preval/(Double.parseDouble(print.getText())));
                        }
                        catch (NumberFormatException ex){
                        result = 0;
                        } break;
                    default: printbis.setText("Error2");
                }
        
        
        printbis.setText(""+result);
        
    }//GEN-LAST:event_eqActionPerformed

